def write_csv():
    moods = [
        (H, "happy", lambda: (round(random.uniform(0.7,0.95),3), round(random.uniform(0.7,0.95),3))),
        (S, "sad", lambda: (round(random.uniform(0.2,0.4),3), round(random.uniform(0.3,0.5),3))),
        (C, "calm", lambda: (round(random.uniform(0.5,0.65),3), round(random.uniform(0.25,0.45),3))),
        (E, "energetic", lambda: (round(random.uniform(0.42,0.58),3), round(random.uniform(0.75,0.95),3))),
        (R, "romantic", lambda: (round(random.uniform(0.4,0.6),3), round(random.uniform(0.5,0.75),3))),
        (A, "angry", lambda: (round(random.uniform(0.15,0.4),3), round(random.uniform(0.6,0.9),3))),
    ]
    
    with open('spotify_songs.csv','w',newline='',encoding='utf-8') as f:
        w = csv.writer(f)
        w.writerow(['','artist_name','genres','followers','artist_popularity','artist_url',
                     'track_name','album_name','release_date','duration_ms','explicit',
                     'track_popularity','danceability','energy','key','loudness','mode',
                     'speechiness','acousticness','instrumentalness','liveness','valence','tempo'])
        idx = 0
        for songs, mood, vfn in moods:
            for artist, track, album, year, dur, pop in songs:
                val, eng = vfn()
                dance = round(random.uniform(0.3,0.9),3)
                tempo = round(random.uniform(70,150),1)
                w.writerow([idx, artist, "Bollywood", random.randint(50000,500000),
                           random.randint(70,95), f'https://open.spotify.com/artist/{idx}',
                           track, album, f'{year}-01-01', dur, False, pop,
                           dance, eng, random.randint(0,11), round(-random.uniform(3,10),3),
                           random.randint(0,1), round(random.uniform(0.02,0.08),4),
                           round(random.uniform(0.01,0.5),4), round(random.uniform(0,0.001),6),
                           round(random.uniform(0.05,0.2),4), val, tempo])
                idx += 1
    print(f"Created CSV with {idx} songs!")
    for songs, mood, _ in moods:
        print(f"  {mood}: {len(songs)} songs")

write_csv()
